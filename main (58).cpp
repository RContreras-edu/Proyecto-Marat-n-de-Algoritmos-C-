#include <iostream>
#include <cmath>
using namespace std;

int main() {
    float peso, altura, imc;
    cout << "Ingrese su peso (kg): ";
    cin >> peso;
    cout << "Ingrese su altura (m): ";
    cin >> altura;

    imc = peso / pow(altura, 2);

    cout << "Su IMC es: " << imc << endl;
    if (imc < 18.5)
        cout << "Bajo peso." << endl;
    else if (imc < 25)
        cout << "Peso normal." << endl;
    else
        cout << "Sobrepeso." << endl;
    return 0;
}
