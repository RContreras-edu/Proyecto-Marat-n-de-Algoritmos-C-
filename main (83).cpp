#include <iostream>
using namespace std;

int main() {
    double base, altura, area;

    cout << "Ingrese la base del rectangulo: ";
    cin >> base;

    cout << "Ingrese la altura del rectangulo: ";
    cin >> altura;

    area = base * altura;

    cout << "El area del rectangulo es: " << area << endl;

    return 0;
}
