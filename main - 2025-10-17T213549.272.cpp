#include <iostream>
using namespace std;

int main() {
    int n;
    cout << "Cuantos animales desea ingresar? ";
    cin >> n;

    int edad, suma = 0;
    for(int i = 0; i < n; i++){
        cout << "Edad del animal " << i+1 << ": ";
        cin >> edad;
        suma += edad;
    }

    double promedio = (double)suma / n;
    cout << "Edad promedio: " << promedio << endl;

    return 0;
}
