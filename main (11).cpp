#include <iostream>
int main() {
    int año;
    std::cout << "Ingresa un año: ";
    std::cin >> año;

    if ((año % 4 == 0 && año % 100 != 0) || (año % 400 == 0)) {
        std::cout << "El año " << año << " es bisiesto." << std::endl;
    } else {
        std::cout << "El año " << año << " no es bisiesto." << std::endl;
    }

    return 0;
}
